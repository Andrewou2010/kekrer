void reset_in_dft(void)
{
    printk("trtr:reset_in_dft\n");

    host_write_trout_reg(
            host_read_trout_reg( (UWORD32)rMAC_PA_CON ) & (~REGBIT0),
            (UWORD32)rMAC_PA_CON );

    write_dot11_phy_reg_sp(0xFF, 0x01);
    write_dot11_phy_reg_sp(0x20, 0xff);
    write_dot11_phy_reg_sp(0xFF, 0x00);

    reset_pacore_sp();
    reset_cecore_sp();
    reset_tsf_sp();
    reset_defrag_lut_sp();
    reset_duplicate_det_sp();


    write_dot11_phy_reg_sp(0xFF, 0x01);
    write_dot11_phy_reg_sp(0x20, 0x00);
    write_dot11_phy_reg_sp(0xFF, 0x00);

    host_write_trout_reg(0x2220,0x8088);

    host_write_trout_reg(
            host_read_trout_reg( (UWORD32)rMAC_PA_CON ) | REGBIT0,
            (UWORD32)rMAC_PA_CON ); 

}

int trout_special_mode(u8 exe_cnt)
{
    unsigned int reg32_val;
    u32 count= exe_cnt;
    int i;

    u32 gainVal[]={0x09,0x08,0xa,0x07,0xb,0x6,0x5,0x4,0x3,0x2,0xc,0xd,0xe,0xf,0x10,0x11,0x12,0x13,0x14};
    host_write_trout_reg(0x02000000,0x8004);

    dft_mac_save();

    //dump_allregs_sp();

    printk("lihua::trout_special_mode Enter 20140223_1616! cnt[%d]\r\n",count);
    printk("lihua::MAC pa Version = 0x%x\r\n", host_read_trout_reg(0x8000));
    printk("lihua::MAC Interrup status = 0x%x\r\n", host_read_trout_reg(0x8580));

    initial_mac_and_phy();

    pa_enable();

    MxdRfSpecialModeIn();

    wifi_tx_pkg();

    host_write_trout_reg(0x00000000,(0x202d<<2));

    dft_test_mode_open();

    {
        for(i=0;i<sizeof(gainVal)/sizeof(gainVal[0]); i++)
        {
            printk("lihua:mxd gain[0x%x]\n",gainVal[i]);
            reset_in_dft();
            MxdRfSModeGainCfg(gainVal[i]);
            msleep(50);
            reg32_val = (host_read_trout_reg((unsigned int)0x8580));
            printk("lihua::MAC Interrup status=0x%x\r\n",reg32_val);

            if (reg32_val&0x01)
            {
                printk("lihua:: RX-Complete Interrupt! cnt[%d] gain[0x%x]\r\n",count,gainVal[i]);
                dft_test_mode_close();
                MxdRfSpecialModeOut();
                dft_mac_restore();
                reset_after_dft();
                //dump_allregs_sp();
                return 0;
            }
            else
            {
                printk("lihua::MAC FCS Fail Counter = 0x%x\r\n", host_read_trout_reg(0x808c));
                printk("lihua::MAC RX End Counter = 0x%x\r\n", host_read_trout_reg(0x8098));
                printk("lihua::MAC RX End Error Counter = 0x%x\r\n", host_read_trout_reg(0x809c));
                printk("lihua::MAC RCV Filter = 0x%x\r\n", host_read_trout_reg(0x8080));
                printk("lihua::MAC error status = 0x%x\r\n", host_read_trout_reg(0x2169<<2));
                printk("lihua::MAC pa control = 0x%x\r\n", host_read_trout_reg(0x8004));
                printk("lihua::MAC rtx complete = 0x%x\r\n", host_read_trout_reg(0x8024));

            }
        }

        printk("lihua::no RX\n");
        dft_test_mode_close();
        MxdRfSpecialModeOut();
        dft_mac_restore();
        reset_after_dft();

        return -1;
    }

    //         panic("lihua::##: trout_special_mode scan failed!\n");


    //dump_allregs_sp();
}


