void read_dot11_phy_reg_sp(UWORD8 ra, UWORD32 *rd)
{
    UWORD32 val = 0;
    UWORD32 delay = 0;

    while((host_read_trout_reg( (UWORD32)rMAC_PHY_REG_ACCESS_CON) & (1<<0)) == 1)
    {
        mdelay(1);
        delay++;

        /* Wait for sometime for the CE-LUT update operation to complete */
        if(delay > 10)
        {
            printk("read phy reg is timeout\n");
            break;
        }
    }

    val  = ((UWORD8)ra << 2); /* Register address set in bits 2 - 9 */
    val |= (1<<1);              /* Read/Write bit set to 1 for read */
    val |= (1<<0);              /* Access bit set to 1 */

    //chenq mod
    //rMAC_PHY_REG_ACCESS_CON = convert_to_le(val);
    host_write_trout_reg( val, (UWORD32)rMAC_PHY_REG_ACCESS_CON );


    while((host_read_trout_reg( (UWORD32)rMAC_PHY_REG_ACCESS_CON)
                & (1<<0)) == 1)
    {
        mdelay(1);
        delay++;
        if(delay > 10)
        {
            printk("read phy reg is timeout\n");
            break;
        }

    }
    *rd = host_read_trout_reg( (UWORD32)rMAC_PHY_REG_RW_DATA); /* Read data from register */
}


void write_dot11_phy_reg_sp(UWORD8 ra, UWORD32 rd)
{
    UWORD32 val   = 0;
    UWORD32 delay = 0;
    UWORD32 v, swich = 0;

    get_phy_mutex();
    read_dot11_phy_reg_sp(0xFF, &v);
    while((host_read_trout_reg( (UWORD32)rMAC_PHY_REG_ACCESS_CON)& (1<<0)) == 1){
        mdelay(1);
        delay++;
        /* Wait for sometime for the CE-LUT update operation to complete */
        if(delay > 10){
            printk("read phy reg is timeout\n");
            break;
        }
    }

    host_write_trout_reg(rd, (UWORD32)rMAC_PHY_REG_RW_DATA);
    val = ((UWORD8)ra << 2); 
    val &= ~ (1<<1);            
    val |= (1<<0);              

    host_write_trout_reg(val, (UWORD32)rMAC_PHY_REG_ACCESS_CON);
    put_phy_mutex();
}

